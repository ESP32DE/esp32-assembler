static const char* boolToString(bool value) {
   return value ? "true" : "false";
}

static void failTest(Testcase *testcase, bool *testFailed) {
   if (!(*testFailed)) {
      *testFailed = true;
      printf("failed (input = \"%s\")\n\n", testcase->input);
   }
}

int main(int argc, char* argv[]) {  	
   size_t processedTestcaseCount = 0;
   size_t failedTestcaseCount    = 0;
   CommandBytes actual;
      
   for (Testcase *testcase = testcases; testcase->input != NULL; testcase++) {
      bool testFailed = false;
      actual.byte0 = 0;
      actual.byte1 = 0;
      actual.byte2 = 0;
      actual.byte3 = 0;
      
      bool result = getCommandBytesFor(testcase->input, &actual);
      
      if (result != testcase->expectedReturnValue) {
         failTest(testcase, &testFailed);
         printf("\tboolean return value    expected: %s\n", boolToString(testcase->expectedReturnValue));
         printf("\t                        actual:   %s\n\n", boolToString(result));
      }

      if ((testcase->expectedReturnValue == true) && 
           (actual.byte0 != testcase->expectedBytes.byte0 || 
            actual.byte1 != testcase->expectedBytes.byte1 || 
            actual.byte2 != testcase->expectedBytes.byte2 || 
            actual.byte3 != testcase->expectedBytes.byte3)) {
         failTest(testcase, &testFailed);
         printf("\tcommand bytes           expected: 0:0x%02x 1:0x%02x 2:0x%02x 3:0x%02x\n", testcase->expectedBytes.byte0, testcase->expectedBytes.byte1, testcase->expectedBytes.byte2, testcase->expectedBytes.byte3);
         printf("\t                        actual:   0:0x%02x 1:0x%02x 2:0x%02x 3:0x%02x\n\n", actual.byte0, actual.byte1, actual.byte2, actual.byte3);
      
      }

      failedTestcaseCount += testFailed ? 1 : 0;
      processedTestcaseCount++;
   }

   if (failedTestcaseCount == 0) {
      printf("\nall %ld testcases succeeded\n\n", processedTestcaseCount);
   } else {
      printf("\n%ld of %ld tests failed\n\n", failedTestcaseCount, processedTestcaseCount);
   }
   
   return 0;
}